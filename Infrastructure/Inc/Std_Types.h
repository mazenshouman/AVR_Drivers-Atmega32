#define NUMBER_TWO                  (uint8)2
#define NUMBER_THREE                (uint8)3
#define NUMBER_FOUR                 (uint8)4
#define NUMBER_FIVE                 (uint8)5
#define NUMBER_SIX                  (uint8)6
#define NUMBER_SEVEN                (uint8)7
#define NUMBER_EIGHT                (uint8)8
#define NUMBER_NINE                 (uint8)9
#define NUMBER_TEN                  (uint8)10


#define STD_LOW                     0x00
#define STD_HIGH                    0x01



#endif /* STD_TYPES_H_ */
